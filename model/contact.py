class Contact:
    def __init__(self,
                 first_name=None,
                 last_name=None,
                 middle_name=None,
                 address=None,
                 email=None,
                 phone_mobile=None):
        self.first_name = first_name
        self.last_name = last_name
        self.middle_name = middle_name
        self.address = address
        self.email = email
        self.phone_mobile = phone_mobile
